def standardize_hk_to_a(df_raw, report_type, a_columns_sample):
    if df_raw is None or df_raw.empty:
        return pd.DataFrame(columns=a_columns_sample)

    # 1. 透视长表转宽表
    df_wide = df_raw.pivot_table(
        index="REPORT_DATE",
        columns="STD_ITEM_NAME",
        values="AMOUNT",
        aggfunc="first"
    ).reset_index()

    df_wide = df_wide.rename(columns={"REPORT_DATE": "报告日"})
    df_wide['报告日'] = pd.to_datetime(df_wide['报告日']).dt.strftime('%Y%m%d')

    # 2. 映射字典选择
    mapper = {"资产负债表": BS_MAP, "利润表": IS_MAP, "现金流量表": CF_MAP}.get(report_type, {})

    # 3. 数据填充：一个港股字段可对应多个A股目标，同一A股目标若有多港股来源则求和
    data_dict = {'报告日': df_wide['报告日'].values}

    for a_col in a_columns_sample:
        if a_col in ['报告日', 'Unnamed: 0']:
            continue

        # 找出所有映射到此A股字段的港股列名
        hk_cols = [hk_key for hk_key, a_val in mapper.items() if a_val == a_col]
        existing_hk_cols = [c for c in hk_cols if c in df_wide.columns]

        if existing_hk_cols:
            data_dict[a_col] = df_wide[existing_hk_cols].apply(pd.to_numeric, errors='coerce').sum(axis=1).values
        else:
            data_dict[a_col] = np.zeros(len(df_wide))

    df_standard = pd.DataFrame(data_dict)

    # ─────────────────────────────────────────────
    # 4. 利润表逻辑补丁
    # ─────────────────────────────────────────────
    if report_type == "利润表":
        # 补丁 A：营业成本 = 营业收入 - 毛利
        if '营业收入' in df_standard.columns and '毛利' in df_wide.columns:
            gross_profit = pd.to_numeric(df_wide['毛利'].fillna(0), errors='coerce').values
            revenue = pd.to_numeric(df_standard['营业收入'], errors='coerce').values
            df_standard['营业成本'] = revenue - gross_profit
            df_standard['营业总成本'] = revenue - gross_profit

        # 补丁 B：信用减值损失 = 资产减值损失中与应收账款相关的部分
        # 港股 '减值及拨备' 合并了资产减值+信用减值（应收款减值）
        # A股 '坏账风险强度' = 信用减值损失 / 应收账款
        # 处理：保守估计信用减值约占总减值的30%-50%，此处取40%作为近似
        # 更精确的方法是拆分原始披露，但通常年报不拆分
        if '信用减值损失' in a_columns_sample and '资产减值损失' in df_standard.columns:
            total_imp = pd.to_numeric(df_standard['资产减值损失'], errors='coerce').fillna(0)
            # 信用减值约为40%（应收类减值），资产减值保留60%（固定资产+存货类减值）
            credit_ratio = 0.40
            df_standard['信用减值损失'] = (total_imp * credit_ratio)
            df_standard['资产减值损失'] = (total_imp * (1 - credit_ratio))

        # 补丁 C：净利润补全（若净利润为0，用归母+少数股东之和填入）
        if '净利润' in df_standard.columns:
            net = pd.to_numeric(df_standard['净利润'], errors='coerce')
            parent = pd.to_numeric(df_standard.get('归属于母公司所有者的净利润', pd.Series(0, index=df_standard.index)), errors='coerce')
            minority = pd.to_numeric(df_standard.get('少数股东损益', pd.Series(0, index=df_standard.index)), errors='coerce')
            df_standard['净利润'] = net.where(net != 0, parent + minority)

    # ─────────────────────────────────────────────
    # 5. 资产负债表逻辑补丁
    # ─────────────────────────────────────────────
    if report_type == "资产负债表":
        # 补丁 A：累计折旧
        # 港股BS原始数据中没有'累计折旧'科目（直接列示净值）
        # 累计折旧由 data_engine._inject_depreciation_to_bs() 估算后注入
        # 本适配器只负责映射，不重复处理

        # 补丁 B：有息负债补全
        # 港股'流动负债其他项目'已映射到'一年内到期的非流动负债'（历史数据验证相关性高）
        # 融资租赁负债(非流动)=租赁负债，不再重复加入有息负债（避免膨胀）
        # 此补丁留作扩展用途（当前暂无需追加操作）

        # 调试输出
        goodwill_alias = [c for c in df_wide.columns if '商' in c or '誉' in c or 'Goodwill' in c]
        print(f"🔎 发现可能的商誉科目: {goodwill_alias}")

    # ─────────────────────────────────────────────
    # 6. 现金流量表逻辑补丁
    # ─────────────────────────────────────────────
    if report_type == "现金流量表":
        # 补丁 A：CAPEX 字段名对齐（双向兜底）
        # main.py 中同时查询"支付的现金"和"所支付的现金"两个写法，A股标准名是"所支付的现金"
        capex_key_1 = '购建固定资产、无形资产和其他长期资产所支付的现金'
        capex_key_2 = '购建固定资产、无形资产和其他长期资产支付的现金'
        if capex_key_1 in df_standard.columns and capex_key_2 not in df_standard.columns:
            df_standard[capex_key_2] = df_standard[capex_key_1]
        elif capex_key_2 in df_standard.columns and capex_key_1 not in df_standard.columns:
            df_standard[capex_key_1] = df_standard[capex_key_2]

        # 补丁 B：经营活动现金流入小计
        # 港股间接法，用 经营活动净额 + 已付税项 近似还原
        if '经营活动现金流入小计' in a_columns_sample:
            ocf_net = pd.to_numeric(df_standard.get('经营活动产生的现金流量净额', pd.Series(0, index=df_standard.index)), errors='coerce')
            tax_paid = pd.to_numeric(df_standard.get('支付的各项税费', pd.Series(0, index=df_standard.index)), errors='coerce')
            int_paid = pd.to_numeric(df_standard.get('支付的其他与经营活动有关的现金', pd.Series(0, index=df_standard.index)), errors='coerce')
            df_standard['经营活动现金流入小计'] = (ocf_net + tax_paid + int_paid).clip(lower=0)

        # 补丁 C：折旧摊销代理（从CF中提取）
        # 港股CF表有时会有"折旧及摊销"科目，用于代理main.py中的累计折旧差值
        # 将该字段临时写入"折旧摊销_CF"列（备用）
        dep_amort_hk_col = [c for c in df_wide.columns if '折旧' in c or '摊销' in c]
        if dep_amort_hk_col and '折旧及摊销' in df_wide.columns:
            dep_amort = pd.to_numeric(df_wide['折旧及摊销'], errors='coerce').fillna(0)
            df_standard['折旧及摊销_CF代理'] = dep_amort.values

    # 7. 集成自动化校验
    verify_integrity(df_standard, report_type)

    # 注意：一定要确保报告日是 Index，否则后续 merge 会找不到列
    return df_standard.set_index('报告日')
