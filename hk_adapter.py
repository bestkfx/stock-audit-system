# ==========================================
# 自动化校验逻辑
# ==========================================

def verify_integrity(df_std, report_type):
    """
    自动化财务勾稽关系校验
    """
    print(f"\n🔍 --- {report_type} 自动化校验报告 ---")
    
    if report_type == "资产负债表":
        if '资产总计' not in df_std.columns or '负债合计' not in df_std.columns:
            print("⚠️ 缺少核心总量字段，无法校验")
            return
        assets = df_std['资产总计']
        liabilities_equity = df_std['负债合计'] + df_std.get('所有者权益(或股东权益)合计', pd.Series(0, index=df_std.index))
        diff = (assets - liabilities_equity).abs()
        max_diff_pct = (diff / assets.replace(0, 1)).max()
        
        if max_diff_pct < 0.01:
            print(f"✅ 资产负债平衡校验通过 (最大偏差率: {max_diff_pct:.4%})")
        else:
            print(f"❌ 资产负债不平衡！最大偏差率: {max_diff_pct:.4%}")

    elif report_type == "利润表":
        if '归属于母公司所有者的净利润' in df_std.columns and '少数股东损益' in df_std.columns and '净利润' in df_std.columns:
            calc_net_profit = df_std['归属于母公司所有者的净利润'] + df_std['少数股东损益']
            diff = (calc_net_profit - df_std['净利润']).abs().max()
            if diff < 1000000:
                print(f"✅ 利润分配勾稽校验通过")
            else:
                print(f"❌ 利润分配不一致！偏差金额: {diff:,.2f}")

    elif report_type == "现金流量表":
        if all(c in df_std.columns for c in ['期末现金及现金等价物余额', '期初现金及现金等价物余额',
                                              '现金及现金等价物净增加额', '汇率变动对现金及现金等价物的影响']):
            calc_change = df_std['期末现金及现金等价物余额'] - df_std['期初现金及现金等价物余额']
            reported_total = df_std['现金及现金等价物净增加额'] + df_std['汇率变动对现金及现金等价物的影响']
            diff = (calc_change - reported_total).abs().max()
            cash_balance = df_std['期末现金及现金等价物余额'].replace(0, 1).max()
            error_rate = diff / cash_balance
            if diff < 1e9 or error_rate < 0.005:
                print(f"✅ 现金流变动勾稽校验通过 (残余偏差: {diff:,.2f}, 误差率: {error_rate:.4%})")
            else:
                print(f"❌ 现金流变动偏差过大: {diff:,.2f}")

    # 通用核心科目非零检查
    critical_checks = {
        "资产负债表": "资产总计",
        "利润表": "营业收入",
        "现金流量表": "经营活动产生的现金流量净额"
    }
    check_col = critical_checks.get(report_type)
    if check_col and check_col in df_std.columns and (df_std[check_col] == 0).all():
        print(f"🚨 严重预警：核心科目 [{check_col}] 全为 0，请检查映射字段名！")


